    Para lidar com estas requisições o PHP nos dá algumas variáveis globais:

    $_GET: variável que recebe os dados enviados por formulários com o método GET;
    $_POST: variável que recebe os dados enviados por formulários com o método POST;
    $_REQUEST: variável que recebe os dados enviados por formulários com o método GET ou POST;
    $_FILES: variável que recebe os dados enviados por formulários com o método POST e que contenham arquivos;
    $_SERVER: variável que recebe informações sobre o servidor;
    $_SESSION: variável que recebe informações sobre a sessão;
    $_COOKIE: variável que recebe informações sobre os cookies;
    $_ENV: variável que recebe informações sobre o ambiente;
    $GLOBALS: variável que recebe todas as variáveis globais do PHP.
