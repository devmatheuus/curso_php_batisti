    Métodos de array:

    array_push($array, $value) - Adiciona um ou mais elementos no final de um array
    array_pop($array) - Remove o último elemento de um array
    array_shift($array) - Remove o primeiro elemento de um array
    array_unshift($array, $value) - Adiciona um ou mais elementos no início de um array
    array_slice($array, $offset, $length) - Retorna uma parte de um array
    array_splice($array, $offset, $length, $replacement) - Remove e substitui elementos de um array
    array_merge($array1, $array2) - Combina um ou mais arrays
    array_combine($keys, $values) - Combina dois arrays, um com as chaves e outro com os valores
    array_sum($array) - Retorna a soma dos valores de um array
    array_product($array) - Retorna o produto dos valores de um array
    array_reverse($array) - Retorna um array com os elementos em ordem inversa
    array_keys($array) - Retorna um array com as chaves de um array
    array_values($array) - Retorna um array com os valores de um array
    array_unique($array) - Remove valores duplicados de um array
    array_flip($array) - Troca as chaves por valores e os valores por chaves
    array_rand($array, $num) - Retorna um ou mais elementos aleatórios de um array
    array_search($value, $array) - Procura por um valor em um array e retorna a chave correspondente
    in_array($value, $array) - Verifica se um valor existe em um array
    sort($array) - Ordena um array em ordem crescente
    rsort($array) - Ordena um array em ordem decrescente
    asort($array) - Ordena um array em ordem crescente, mantendo a associação entre chaves e valores
    arsort($array) - Ordena um array em ordem decrescente, mantendo a associação entre chaves e valores
    ksort($array) - Ordena um array em ordem crescente, baseado nas chaves
    krsort($array) - Ordena um array em ordem decrescente, baseado nas chaves
    natsort($array) - Ordena um array em ordem crescente, usando algoritmo "natural order"
    natcasesort($array) - Ordena um array em ordem crescente, usando algoritmo "natural order" e ignorando maiúsculas e minúsculas
    usort($array, $function) - Ordena um array em ordem crescente, usando uma função de comparação definida pelo usuário
    uasort($array, $function) - Ordena um array em ordem crescente, mantendo a associação entre chaves e valores, usando uma função de comparação definida pelo usuário
    range($start, $end, $step) - Podemos utilizar a função range para criar um array de forma rápida 
    count($array) - Retorna o número de elementos dentro do array
    list($var) = $array - função usada para descontrução
    array_chunk($array, $numDeArrays) - Divide um array grande em diversos arrays menores com o número de elementos iguais
    array_key_exists("nome", $array) - Verifica se há um valor em uma respectiva chave de um array
    isset($array['chave']) - Verifica se há um valor em uma respectiva chave de um array (também pode ser usado com variáveis)
    suffle($array) - Modifica a estrutura original do array e retorna em ordem aleatória
